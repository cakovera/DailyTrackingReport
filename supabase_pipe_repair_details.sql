create table if not exists public.pipe_repair_details (
    id bigint generated by default as identity primary key,
    date date not null,
    project_sheet text not null,
    block_cell text not null,
    pipe_no integer not null,
    pipe_length_ft double precision,
    repair_amount double precision not null,
    repair_ratio double precision not null check (repair_ratio >= 0 and repair_ratio <= 1),
    repair_count integer,
    repair_category text not null,
    surface_state text not null,
    created_at timestamptz not null default now(),
    updated_at timestamptz not null default now(),
    constraint uq_pipe_repair_details_day_sheet_cell unique (date, project_sheet, block_cell)
);

create index if not exists idx_pipe_repair_details_date
on public.pipe_repair_details (date);

create index if not exists idx_pipe_repair_details_project_sheet
on public.pipe_repair_details (project_sheet);

alter table public.pipe_repair_details enable row level security;

grant select, insert, update on public.pipe_repair_details to anon;
grant usage, select on all sequences in schema public to anon;

drop policy if exists "pipe_repair_details_select_anon" on public.pipe_repair_details;
create policy "pipe_repair_details_select_anon"
on public.pipe_repair_details
for select
to anon
using (true);

drop policy if exists "pipe_repair_details_insert_anon" on public.pipe_repair_details;
create policy "pipe_repair_details_insert_anon"
on public.pipe_repair_details
for insert
to anon
with check (true);

drop policy if exists "pipe_repair_details_update_anon" on public.pipe_repair_details;
create policy "pipe_repair_details_update_anon"
on public.pipe_repair_details
for update
to anon
using (true)
with check (true);
